		<p>Grâce à mon <b>experience</b> et nombreux projets, je peux planifier et organiser la <b>conception</b> et le développement d'un produit dans sa globalité, utiliser de bonnes pratiques comme les tests unitaires et d'intégrations, les méthodologies Git, l'organisation agile...</p>
		<p>J'ai également <b>une vue très large</b> de l'informatique, et je comprend de nombreuses technologies de l'Assembleur au Typescript en passant par Docker.</p>
		<p>Ayant également d'autres passions comme le sport ou la musique, j'ai égalament beaucoup de recul sur la technique, et mon principal fil conducteur est que <b>la technique est un outil</b> surpuissant au service de la création, de l'automatisation et de la société en générale.</p>
