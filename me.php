		<p>Né à Lyon et de nature très curieuse, j'ai passé mon enfance à <b>découvrir, essayer, hacker, créer</b>. Je suis tombé dans le code très tôt, dès l'école primaire. La création informatique est ma <b>passion</b>.</p>
